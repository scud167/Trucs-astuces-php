<?php

/**
 * Fait la somme des 2 chiffres passé en parametre
 * @param int $a Premier chiffre
 * @param int $b Second chiffre
 * @return int Somme de $a + $b
 */
function somme($a,$b){
    return $a+b;
}
/*
passez la souris sur les parentheses pour obtenir une description de la fonction est des parametres 
*/
somme(1,2);

